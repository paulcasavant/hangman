#include "Body.h"

Body::Body()
{
    _condition = ZERO_PARTS;
}

Body::~Body(){};

void Body::removePart()
{
    // If not dead, add more parts to gallows
    if (!isDead())
    {
        _condition--;
    }
}

void Body::setFree()
{
    _condition = FREE;
}

bool Body::isDead() const
{
    return _condition == DEAD;
}

void Body::reset()
{
    _condition = ZERO_PARTS;
}

int Body::getCondition() const
{
    return _condition;
}

